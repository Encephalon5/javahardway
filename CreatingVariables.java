public class CreatingVariables {
  public static void main( String[] args) {
    int x, y, age, satan;
    double seconds, e, checking, pi;
    String firstName, lastName, title, placeofBirth, dateofBirth;

    x = 10;
    y = 400;
    age = 39;
    satan = 666;

    seconds = 4.71;
    e = 2.71828182845904523536;
    checking = 1.89;
    pi = 3.141592654;

    firstName = "Graham";
    lastName = "Mitchell";
    title = "Mr.";
    placeofBirth = "North Carolina";
    dateofBirth = "06-03-1993";

    System.out.println( "The variable x contains " + x );
    System.out.println( "The value " + y + " is stored in the variable y." );
    System.out.println( "Let him who hath understanding reckon the number of The Beast, for it is a human number. It's number is: " + satan);
    System.out.println( "The experiment took " + seconds + "seconds.");
    System.out.println( "A favorite irrational # is Eulers' number: " + e );
    System.out.println( "Another favorite irrational # is:" + pi );
    System.out.println( "Hopefully you have more than $" + checking + "1" );
    System.out.println( "My name's " + title + " " + firstName + lastName );
    System.out.println( "I was born in " + placeofBirth );
    System.out.println( "My birthdate is " + dateofBirth );
  }
}
